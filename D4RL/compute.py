import numpy as np


def calculate_overall_stats(means, stds, n):
    """
    计算总体的平均分数和标准差。

    参数:
    - means: 每组的平均分数列表。
    - stds: 每组的标准差列表。
    - n: 每组的样本数量。

    返回:
    - overall_mean: 总体平均分数。
    - overall_std: 总体标准差。
    """
    # 计算加权平均分数
    overall_mean = np.mean(means)

    # 计算总体方差，考虑到每组的方差和组内方差
    variances = [std ** 2 for std in stds]
    overall_variance = np.mean(variances) + np.var(means)

    # 计算总体标准差
    overall_std = np.sqrt(overall_variance)

    return overall_mean, overall_std


